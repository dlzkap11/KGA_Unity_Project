using UnityEngine;

public class Particle : MonoBehaviour
{
    /*
     * 오후 실습

통통 튀는 공 사격장
Sphere 여러 개로 다음을 구현합니다.

클릭할 때마다 새 공이 하나씩 생성되어(Instantiate) 클릭한 방향으로 Impulse 힘을 받아 날아간다
각 공은 Mass가 서로 다르게 랜덤 부여되어, 같은 힘에도 날아가는 정도가 제각각이다
발사와 동시에 AddTorque로 회전력도 받아 빙글빙글 돌면서 날아간다
Physic Material로 바닥과 벽에 부딪힐 때마다 튀어오르며, 부딪힐 때마다 속도에 비례해 소리 크기가 달라진다
부딪히는 대상이 바닥인지 벽인지 다른 공인지에 따라 서로 다른 색의 파티클이 재생된다
정해진 목표 구역에 공이 안착하면 성공 이펙트와 성공음이 재생되고, 그 공은 더 이상 반응하지 않도록 처리된다
공이 일정 횟수 이상 튀거나, 일정 시간이 지나면 스스로 사라진다

다중 감지 함정 지대
플레이어와 두 종류 이상의 함정으로 다음을 구현합니다.

함정은 가벼운 함정과 강한 함정 두 종류로 나뉘고, 각각 다른 Layer를 가진다
플레이어 정면뿐 아니라 좌/우로도 동시에 Raycast를 쏴서, 여러 각도의 함정을 한꺼번에 감지한다
각 방향의 감지 여부와 거리를 구분해서 처리하며, 가장 가까운 함정을 기준으로 경고음의 재생 간격이 거리에 비례해 빨라진다
실제로 함정에 닿으면 함정 종류에 따라 넉백 세기가 다르게 적용되고, 타격 이펙트와 소리가 함께 재생된다
강한 함정에 닿는 순간 AddExplosionForce가 발생해 주변의 가벼운 함정들까지 밀려나는 연쇄 반응이 일어난다
플레이어가 함정에 일정 횟수 이상 부딪히면 일정 시간 동안 움직일 수 없는 경직 상태가 된다
안전지대(Trigger)에 들어가면 지금까지 누적된 피격 횟수와 경직 상태가 모두 초기화된다
감지/피격 관련 모든 상태 변화(감지 시작, 피격, 경직, 초기화)가 Console에 구분되어 기록된다

다중 타워 공략전
탑 두 개 이상과 플레이어로 다음을 구현합니다.

각 탑은 최소 9개 이상의 상자로 3층 이상 쌓여 있고, 일부 상자는 Unbreakable Layer로 지정되어 무너지지 않는 기둥 역할을 한다
왼쪽 클릭은 클릭 지점에 Raycast로 정확히 하나의 상자를 맞혀 강한 Impulse로 날리고, 오른쪽 클릭은 클릭 지점 주변에 AddExplosionForce를 일으켜 여러 상자를 한꺼번에 흩어버린다
상자마다 Mass가 다르게 설정되어 있어 같은 힘에도 날아가는 정도가 다르며, 부딪힐 때마다 속도에 비례한 소리와 파티클이 재생된다
각 탑은 독립적으로 붕괴 여부를 판정한다 (원래 위치에서 일정 거리 이상 벗어난 상자 비율로 판정하며, 한 탑이 무너져도 다른 탑은 그대로 유지되어야 한다)
탑이 무너지는 순간 해당 탑에서만 파괴 이펙트와 축하 사운드가 재생된다
전체 시도(클릭) 횟수가 정해진 한도를 넘으면 아직 무너지지 않은 탑이 있어도 실패로 판정되고, 모든 탑이 그 한도 안에 무너지면 클리어로 판정된다
특정 키를 누르면 모든 상자가 원래 위치와 회전으로 되돌아가고, 시도 횟수와 각 탑의 붕괴 상태도 전부 초기화된다
남은 탑 개수, 현재 시도 횟수, 각 탑의 생존 상자 비율이 매 판정 시점마다 Console에 기록된다
    */
    ParticleSystem particle;
    GameObject particlePrefab;
    void Start()
    {
        if(particle == null)
        {
            particle = Instantiate(particlePrefab, particlePrefab.transform.position, Quaternion.identity).GetComponent<ParticleSystem>();
        }
    }

    
    void Update()
    {
        if (Input.GetKeyDown(KeyCode.J))
        {
            particle.Play();
        }
    }
}
